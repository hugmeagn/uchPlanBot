"""
Сервис для ручного ввода группы.
"""


def create_group_input_message() -> str:
    """
    Создает сообщение для ввода группы.

    Returns:
        Текст сообщения
    """
    return (
        "Ваше заведение найдено!\n\n"
        "👥 **Ввод группы**\n\n"
        "Введите название вашей учебной группы:\n\n"
        "**Примеры:**\n"
        "• ИБ-21 (информационная безопасность)\n"
        "• ПКС-20-2 (программисты)\n"
        "• МОАИС-19 (мобильные системы)\n"
        "• ЭС-22 (электрики)\n\n"
        "Можно вводить в любом формате."
    )


def normalize_group_name(group: str) -> str:
    """
    Простая нормализация названия группы.

    Args:
        group: Исходное название группы

    Returns:
        Нормализованное название группы
    """
    # Убираем лишние пробелы
    return ' '.join(group.strip().split())
